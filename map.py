class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.image_block = pg.image.load('Images/block_1.png')
        self.image_wall = pg.image.load('Images/wall.png')
        self.cell_size = 50
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.block_map = {}
        self.wall_map = {}
        self.get_map()
        self.create_blocks()
        self.bonus_timer = 600

    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                pass
                if value == 1:
                    self.block_map[(i, j)] = value
                if value == 0:
                    self.wall_map[(i, j)] = value

    def create_blocks(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value == 0:
                    Block(self.game, self, i * self.cell_size, j * self.cell_size)

    def draw(self):
        [self.game.screen.blit(self.image_block, (pos[0] * self.cell_size, pos[1] * self.cell_size,
                                                  self.cell_size, self.cell_size)) for pos in self.block_map]

        [self.game.screen.blit(self.image_wall, (pos[0] * self.cell_size, pos[1] * self.cell_size,
                                                 self.cell_size, self.cell_size)) for pos in self.wall_map]

    def spawn_bonus(self):
        if self.bonus_timer > 0:
            self.bonus_timer -= 1
        else:
            self.create_bonus()
            self.bonus_timer = 300

    def create_bonus(self):
        m = random.randint(0, self.rows - 1)
        n = random.randint(0, self.cols - 1)

        px = n * self.cell_size
        py = m * self.cell_size

        if self.mini_map[m][n] == 1:
            Bonus(self.game, px, py)
        else:
            self.create_bonus()
